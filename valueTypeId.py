# Everything in Python is an object
# Every object in python has a value, a type, and an id

word = "bird"

print(word)
print(type(word))
print(id(word))
# bird
# <class 'str'>
# 140680415716080

nice = 69
print(nice)
print(type(nice))
print(id(nice))
# 69
# <class 'int'>
# 9795232

jigaWatts = 1.21
print(jigaWatts)
print(type(jigaWatts))
print(id(jigaWatts))
# 1.21
# <class 'float'>
# 140131613117136